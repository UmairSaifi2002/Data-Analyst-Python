str  = 'Welcome to Python Programming!'
print(str)
print(str[0])  # First character
print(str[1])  # Second character
print(str[3])  # Third character
print(str[-2]) # Second last character

print(str[2:8])  # From index 2 to 8
print(str[::-1])   # reeverse the string
print(str[-5:-8:-1])  # substring using negative index

