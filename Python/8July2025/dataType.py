# integer types
n1 = 5 # simple data type
print(type(n1))

n2 = 5.0 # float data type
print(type(n2))

n3 = 5 + 2j # complex data type
print(type(n3))

# String data type
str = 'Hello Umair.'
print(type(str))

# Boolean data type
bool_value = True
print(type(bool_value))

# set data type
set1 = {2,1,3,4,5} 
print(set1)
print(type(set1)) # <class 'set'>
set_value = set([23,45,63,78,93,54, 67, 19,19,23])
print(set_value) # {67, 45, 78, 19, 54, 23, 93, 63}
print(type(set_value)) # <class 'set>